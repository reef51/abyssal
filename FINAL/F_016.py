# Calculate the probability that a sales report is incomplete and on-time

# Given probabilities
P_incomplete = 0.10
P_on_time = 0.90

# Calculate the probability of incomplete and on-time
P_incomplete_and_on_time = P_incomplete * P_on_time

# Output the result
print(f"Probability that the sales report is incomplete and on-time: {P_incomplete_and_on_time:.2f}")